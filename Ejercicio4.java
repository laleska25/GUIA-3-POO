    private void calButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calButtonActionPerformed

        imcArea.setText(null);
        
        float peso;
        float altura;
        double imc;
        peso = Float.parseFloat(this.pesoText.getText());
        altura = Float.parseFloat(this.altuText.getText());
        
        //ALGORITMO
        
        imc = (peso / Math.pow(altura,2));

        if (imc >= 27){
            this.imcArea.append("Su grado de obesidad es: OBESIDAD");
        }else if (imc>=25 && imc<27){
            this.imcArea.append("Su grado de obesidad es: SOBREPESO");
        }else if (imc>=20 && imc<25){
            this.imcArea.append("Su grado de obesidad es: NORMAL");
        }else {
            this.imcArea.append("Su grado de obesidad es: DELGADO");
        }
    }//GEN-LAST:event_calButtonActionPerformed

